// Command gen emits both lifeproto bindings from the schema in spec.go:
// wire/lifeproto/lifeproto.go and Sources/DaemonKit/LifecycleWire.swift.
// Run it via `go generate ./wire/...`.
package main

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

const genHeader = "// Code generated by wire/lifeproto/gen; DO NOT EDIT."

func main() {
	root, err := moduleRoot()
	if err != nil {
		fail(err)
	}
	goSrc, err := format.Source([]byte(renderGo(lifeproto)))
	if err != nil {
		fail(fmt.Errorf("gofmt generated Go: %w", err))
	}
	writeFile(filepath.Join(root, "wire", "lifeproto", "lifeproto.go"), goSrc)
	writeFile(filepath.Join(root, "Sources", "DaemonKit", "LifecycleWire.swift"), []byte(renderSwift(lifeproto)))
}

func moduleRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("gen: no go.mod above %s", dir)
		}
		dir = parent
	}
}

func writeFile(path string, data []byte) {
	if err := os.WriteFile(path, data, 0o600); err != nil {
		fail(fmt.Errorf("write %s: %w", path, err))
	}
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}

// renderGo builds the Go binding source; the caller runs it through gofmt.
func renderGo(s schema) string {
	var b strings.Builder
	fmt.Fprintf(&b, "%s\n\n", genHeader)
	b.WriteString("package lifeproto\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"encoding/json\"\n")
	b.WriteString("\t\"errors\"\n")
	b.WriteString("\t\"fmt\"\n")
	b.WriteString(")\n\n")

	b.WriteString("// Version is the lifecycle protocol version carried in every envelope's \"v\"\n")
	b.WriteString("// field. The Swift peer pins the same constant.\n")
	fmt.Fprintf(&b, "const Version = %d\n\n", s.version)

	b.WriteString("// The op strings name each lifecycle operation. Frozen wire values.\n")
	b.WriteString("const (\n")
	for _, o := range s.ops {
		fmt.Fprintf(&b, "\t%s = %q\n", o.constName, o.value)
	}
	b.WriteString(")\n\n")

	b.WriteString("// ErrProtocolVersion means a decoded envelope carried a \"v\" other than Version.\n")
	b.WriteString("var ErrProtocolVersion = errors.New(\"lifeproto: unsupported protocol version\")\n\n")

	b.WriteString("// Envelope is the {v, op} header every message begins with; ReadEnvelope decodes\n")
	b.WriteString("// it to dispatch on Op before the concrete request type is known.\n")
	b.WriteString("type Envelope struct {\n")
	b.WriteString("\tV  int    `json:\"v\"`\n")
	b.WriteString("\tOp string `json:\"op\"`\n")
	b.WriteString("}\n\n")

	for _, m := range s.messages {
		renderGoMessage(&b, m)
	}

	b.WriteString("// Encode marshals one lifecycle message for a v2 request or response payload.\n")
	b.WriteString("func Encode(msg any) ([]byte, error) {\n")
	b.WriteString("\tb, err := json.Marshal(msg)\n")
	b.WriteString("\tif err != nil {\n\t\treturn nil, fmt.Errorf(\"lifeproto: encode: %w\", err)\n\t}\n")
	b.WriteString("\treturn b, nil\n}\n\n")

	b.WriteString("// DecodeEnvelope decodes {v, op} and rejects every foreign protocol version.\n")
	b.WriteString("func DecodeEnvelope(b []byte) (Envelope, error) {\n")
	b.WriteString("\tvar e Envelope\n")
	b.WriteString("\tif err := json.Unmarshal(b, &e); err != nil {\n")
	b.WriteString("\t\treturn Envelope{}, fmt.Errorf(\"lifeproto: decode envelope: %w\", err)\n")
	b.WriteString("\t}\n")
	b.WriteString("\tif e.V != Version {\n")
	b.WriteString("\t\treturn Envelope{}, fmt.Errorf(\"%w: got v=%d, want %d\", ErrProtocolVersion, e.V, Version)\n")
	b.WriteString("\t}\n\treturn e, nil\n}\n")

	return b.String()
}

func renderGoMessage(b *strings.Builder, m message) {
	fmt.Fprintf(b, "// %s\n", m.typeDoc)
	fmt.Fprintf(b, "type %s struct {\n", m.name)
	b.WriteString("\tV  int    `json:\"v\"`\n")
	b.WriteString("\tOp string `json:\"op\"`\n")
	for _, f := range m.fields {
		fmt.Fprintf(b, "\t%s %s `json:%q`\n", f.goName, f.kind.goType, f.json)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "// New%s %s\n", m.name, m.ctorDoc)
	params := make([]string, 0, len(m.fields))
	for _, f := range m.fields {
		params = append(params, fmt.Sprintf("%s %s", f.name, f.kind.goType))
	}
	fmt.Fprintf(b, "func New%s(%s) %s {\n", m.name, strings.Join(params, ", "), m.name)
	assigns := make([]string, 0, 2+len(m.fields))
	assigns = append(assigns, "V: Version", fmt.Sprintf("Op: %s", m.opConst))
	for _, f := range m.fields {
		val := f.name
		if f.kind.slice {
			val = fmt.Sprintf("nonNil(%s)", f.name)
		}
		assigns = append(assigns, fmt.Sprintf("%s: %s", f.goName, val))
	}
	fmt.Fprintf(b, "\treturn %s{%s}\n}\n\n", m.name, strings.Join(assigns, ", "))
}

// Foundation's JSONEncoder cannot reproduce Go's struct-order marshaling
// (.sortedKeys sorts alphabetically), so Swift encodes by hand in wire order.
const swiftStatic = "" +
	`/// Assembles flat lifecycle frames in Go's ` + "`json.Marshal`" + ` field order
/// with Go-compatible string escaping, so the Swift bytes match the Go peer
/// exactly. Foundation's JSONEncoder is not used to encode: it does not preserve
/// key order.
enum LifecycleJSON {
    static func object(_ members: [(String, String)]) -> Data {
        var out = "{"
        for (index, member) in members.enumerated() {
            if index > 0 { out += "," }
            out += escape(member.0) + ":" + member.1
        }
        out += "}"
        return Data(out.utf8)
    }

    static func string(_ value: String) -> String { escape(value) }

    static func int(_ value: Int) -> String { String(value) }

    static func bool(_ value: Bool) -> String { value ? "true" : "false" }

    static func strings(_ values: [String]) -> String {
        "[" + values.map(escape).joined(separator: ",") + "]"
    }

    // Mirrors encoding/json HTML-safe escaping so the Swift bytes match the
    // Go peer for any string value (quotes, backslash, control, and HTML bytes).
    static func escape(_ value: String) -> String {
        var out = "\""
        for scalar in value.unicodeScalars {
            switch scalar {
            case "\"": out += "\\\""
            case "\\": out += "\\\\"
            case "\n": out += "\\n"
            case "\r": out += "\\r"
            case "\t": out += "\\t"
            case "<": out += "\\u003c"
            case ">": out += "\\u003e"
            case "&": out += "\\u0026"
            case "\u{2028}": out += "\\u2028"
            case "\u{2029}": out += "\\u2029"
            default:
                if scalar.value < 0x20 {
                    out += String(format: "\\u%04x", scalar.value)
                } else {
                    out.unicodeScalars.append(scalar)
                }
            }
        }
        out += "\""
        return out
    }
}

/// A flat lifecycle message: the shared ` + "`{v, op}`" + ` header plus op-specific
/// fields, carried as a v2 request or response payload.
public protocol LifecycleMessage: Decodable, Sendable {
    /// The compact JSON payload encoding of this message.
    func encoded() -> Data
}

public extension LifecycleMessage {
    /// Decodes a message from one v2 frame payload.
    static func decode(from data: Data) throws -> Self {
        try JSONDecoder().decode(Self.self, from: data)
    }
}

/// A decoded frame whose protocol version is not ` + "``DaemonKit/lifeProtocolVersion``" + `.
public enum LifecycleWireError: Error, Sendable {
    case unsupportedProtocolVersion(Int)
}

/// The ` + "`{v, op}`" + ` header every message begins with; ` + "``decode(from:)``" + ` reads it to
/// dispatch on ` + "`op`" + ` before the concrete message type is known.
public struct Envelope: Decodable, Sendable {
    public let v: Int
    public let op: String

    /// Decodes the header and rejects a foreign protocol version with
    /// ` + "``LifecycleWireError/unsupportedProtocolVersion(_:)``" + `.
    public static func decode(from data: Data) throws -> Envelope {
        let envelope = try JSONDecoder().decode(Envelope.self, from: data)
        guard envelope.v == DaemonKit.lifeProtocolVersion else {
            throw LifecycleWireError.unsupportedProtocolVersion(envelope.v)
        }
        return envelope
    }
}
`

// renderSwift builds the Swift binding source, checked in as-is (excluded
// from swiftformat/swiftlint) and pinned byte-stable by the CI drift gate.
func renderSwift(s schema) string {
	var b strings.Builder
	fmt.Fprintf(&b, "%s\n\n", genHeader)
	b.WriteString("import Foundation\n\n")

	b.WriteString("public extension DaemonKit {\n")
	b.WriteString("    /// Version of the daemonkit-native lifecycle envelope (`\"v\"` in every flat\n")
	b.WriteString("    /// frame). Generated from the shared lifeproto schema; the Go side pins the\n")
	b.WriteString("    /// same `Version`.\n")
	fmt.Fprintf(&b, "    static let lifeProtocolVersion = %d\n", s.version)
	b.WriteString("}\n\n")

	b.WriteString("/// A canonical lifecycle operation carried in a message's `op` field.\n")
	b.WriteString("public enum LifecycleOp: String, Sendable, CaseIterable {\n")
	for _, o := range s.ops {
		fmt.Fprintf(&b, "    case %s\n", o.value)
	}
	b.WriteString("}\n\n")

	b.WriteString(swiftStatic)

	for _, m := range s.messages {
		b.WriteString("\n")
		renderSwiftMessage(&b, m)
	}
	return b.String()
}

func renderSwiftMessage(b *strings.Builder, m message) {
	fmt.Fprintf(b, "/// %s\n", m.typeDoc)
	fmt.Fprintf(b, "public struct %s: LifecycleMessage {\n", m.name)
	b.WriteString("    public let v: Int\n")
	b.WriteString("    public let op: String\n")
	for _, f := range m.fields {
		fmt.Fprintf(b, "    /// %s\n", f.doc)
		fmt.Fprintf(b, "    public let %s: %s\n", f.name, f.kind.swiftType)
	}
	b.WriteString("\n    private enum CodingKeys: String, CodingKey {\n")
	b.WriteString("        case v\n")
	b.WriteString("        case op\n")
	for _, f := range m.fields {
		if f.name == f.json {
			fmt.Fprintf(b, "        case %s\n", f.name)
		} else {
			fmt.Fprintf(b, "        case %s = %q\n", f.name, f.json)
		}
	}
	b.WriteString("    }\n")
	b.WriteString("\n")

	params := make([]string, 0, len(m.fields))
	for _, f := range m.fields {
		params = append(params, fmt.Sprintf("%s: %s", f.name, f.kind.swiftType))
	}
	fmt.Fprintf(b, "    /// %s\n", swiftCtorDoc(m))
	fmt.Fprintf(b, "    public init(%s) {\n", strings.Join(params, ", "))
	b.WriteString("        v = DaemonKit.lifeProtocolVersion\n")
	fmt.Fprintf(b, "        op = LifecycleOp.%s.rawValue\n", m.op)
	for _, f := range m.fields {
		fmt.Fprintf(b, "        self.%s = %s\n", f.name, f.name)
	}
	b.WriteString("    }\n\n")

	b.WriteString("    public func encoded() -> Data {\n")
	b.WriteString("        LifecycleJSON.object([\n")
	b.WriteString("            (\"v\", LifecycleJSON.int(v)),\n")
	b.WriteString("            (\"op\", LifecycleJSON.string(op)),\n")
	for _, f := range m.fields {
		fmt.Fprintf(b, "            (%q, LifecycleJSON.%s(%s)),\n", f.json, swiftEncoder(f.kind), f.name)
	}
	b.WriteString("        ])\n")
	b.WriteString("    }\n")
	b.WriteString("}\n")
}

func swiftEncoder(k fieldKind) string {
	switch {
	case k.slice:
		return "strings"
	case k.goType == "int":
		return "int"
	case k.goType == "bool":
		return "bool"
	default:
		return "string"
	}
}

func swiftCtorDoc(m message) string {
	return strings.ToUpper(m.ctorDoc[:1]) + m.ctorDoc[1:]
}
